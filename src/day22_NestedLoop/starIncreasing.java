package day22_NestedLoop;

public class starIncreasing {
    public static void main(String[] args) {

        for(int i =1;i<=7;i++){
            for(int k=1;k<=i;k++){
                System.out.print("*");
            }
            System.out.println();}
        System.out.println("======================");

        for(int j=7;j>=1;j--){for(int k=j;k>=1;k--){
            System.out.print("*");
        }
            System.out.println();




        }


    }
}
