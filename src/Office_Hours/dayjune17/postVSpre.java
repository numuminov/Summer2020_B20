package Office_Hours.dayjune17;

public class postVSpre {
    public static void main(String[] args) {
        int a = 300;
        System.out.println( ++a);
        //301
        System.out.println(++a);
        //302
        int b = 50;
        System.out.println(--b);//49
        System.out.println(--b);//48
        System.out.println("==============");
        int x = 40;
        System.out.println(x--);//40
        System.out.println(x);//39
        System.out.println(x--);//39
        System.out.println(x);//38

        int y=60;

        System.out.println(y++);//60
        System.out.println(y);//61
        System.out.println(y++);//61
        System.out.println(y);//62



    }
}
