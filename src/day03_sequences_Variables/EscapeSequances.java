package day03_sequences_Variables;

public class EscapeSequances {
    public static void main(String[] args) {
        System.out.println("Hello Everyone \nMy name is Cybertek"); // new line to message
        System.out.println("\tI am in Virginia"); // paragraph spice
        System.out.println("\n\nI love programming");
        System.out.println("\\"); // this prints one back slash
        System.out.println("/"); // this prints one front slash
        System.out.println("My name is \'Muhtar\'");
        System.out.println("My favorite TV-series: \"Game of Thrones\"");
        System.out.println("");



    }
}
