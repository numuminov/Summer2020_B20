package day19_ForLoop;

public class Characters_A_Z {
    public static void main(String[] args) {

        for(char ch ='A';ch<='Z';ch++){
            System.out.print(ch);}


        for(int i =97;i<=122;i++){
            System.out.print((char)i+" ");}

        System.out.println();


        for(char ch ='Z'; ch>='A';ch--){
            System.out.print(ch+ " ");}



        for(int i =0; i<6500;i++){
            System.out.print((char)i+" ");}






}}
