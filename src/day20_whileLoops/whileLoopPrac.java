package day20_whileLoops;

public class whileLoopPrac {
    public static void main(String[] args) {
        for(int i =1;i<=10;i++){
            System.out.print(i+" ");
        }
        System.out.println();
        int i =1;

        while (i<=10){


            System.out.print(i+" ");

            i++;
        }




    }
}
